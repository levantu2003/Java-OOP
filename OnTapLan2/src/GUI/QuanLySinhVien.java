    private void fillDatatoTable() {
        CDController cdcontroller = new CDController();
        st = cdcontroller.docTatCaCD();

        tbModel = (DefaultTableModel) tbSinhVien.getModel();
        tbModel.setRowCount(0);

        for (students sv : st) {
            Object[] row = new Object[]{sv.getMasv(), sv.getHoten(), sv.getEmail(), sv.getSodt(), sv.getGioitinh(), sv.getDiachi(), sv.getHinh()};
            tbModel.addRow(row);
        }
        tbSinhVien.setModel(tbModel);
    }

