    private void tbSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSinhVienMouseClicked
        index = tbSinhVien.getSelectedRow();
        if (index <= -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa thêm 1 dòng vào bảng");
            return;
        } else {
            students cd = new students();
            cd = st.get(index);
            txtMaSV.setText(cd.getMasv());
            txtHoTen.setText(cd.getHoten());
            txtSoDT.setText(cd.getSodt());
            txtEmail.setText(cd.getEmail());
            txtDiaChi.setText(cd.getDiachi());
            if (cd.getGioitinh() == 1) {
                rdNam.setSelected(true);
                rdNu.setSelected(false);
            } else {
                rdNam.setSelected(false);
                rdNu.setSelected(true);
            }
        }
    }//GEN-LAST:event_tbSinhVienMouseClicked

