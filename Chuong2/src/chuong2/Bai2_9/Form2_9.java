    private void btnDSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDSaveActionPerformed
        String code = this.txtDCode.getText().trim().toUpperCase();
        txtDCode.setText(code);
        String name = this.txtDName.getText().trim();
        txtDName.setText(name);
        if (!validDepDetails()) {
            return;
        }
        if (addNewDep == true) {
            Department newDep = new Department(code, name);
            root.add(new DefaultMutableTreeNode(newDep));
        } else {
            ((Department) curDepNode.getUserObject()).setDepName(name);
        }
        this.cayQL.updateUI();
        this.addNewDep = false;
    }//GEN-LAST:event_btnDSaveActionPerformed

