    private void loadData() {
        String str = "";
        StringTokenizer stk;
        try {
            FileReader f = new FileReader(filename);
            BufferedReader bf = new BufferedReader(f);
            while ((str = bf.readLine()) != null) {
                str = str.trim();
                boolean isDept = (str.charAt(str.length() - 1) == ':');
                stk = new StringTokenizer(str, "-:,");
                String code = stk.nextToken().trim();
                String name = stk.nextToken().trim();
                if (isDept) {
                    curDepNode = new DefaultMutableTreeNode(new Department(code, name));
                    root.add(curDepNode);
                } else {
                    int salary = Integer.parseInt(stk.nextToken().trim());
                    curEmpNode = new DefaultMutableTreeNode(new Employee(code, name, salary));
                    curDepNode.add(curEmpNode);
                }
            }
            bf.close();
            f.close();
        } catch (IOException | NumberFormatException e) {
        }
    }

    private void viewDeptAndEmp() {
        Department curDep = null;
        Employee curEmp = null;
        if (curDepNode != null) {
            curDep = (Department) (curDepNode.getUserObject());
        }
        if (curEmpNode != null) {
            curEmp = (Employee) (curEmpNode.getUserObject());
        }
        this.txtDCode.setText(curDep != null ? curDep.getDepCode() : "");
        this.txtDName.setText(curDep != null ? curDep.getDepName() : "");
        this.txtECode.setText(curEmp != null ? curEmp.getEmpCode() : "");
        this.txtEName.setText(curEmp != null ? curEmp.getEmpName() : "");
        this.txtESalary.setText("" + (curEmp != null ? curEmp.getSalary() : ""));
        this.txtDCode.setEditable(false);
        this.txtECode.setEditable(false);
    }

