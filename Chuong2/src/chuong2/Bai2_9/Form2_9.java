    private void btnESaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnESaveActionPerformed
        String code = this.txtECode.getText().trim().toUpperCase();
        txtECode.setText(code);
        String name = this.txtEName.getText().trim();
        txtEName.setText(name);
        String salaryStr = this.txtESalary.getText().trim();
        txtESalary.setText(salaryStr);
        int sal = Integer.parseInt(salaryStr);
        if (!validEmpDetails()) {
            return;
        }
        if (addNewEmp == true) {
            Employee newEmp = new Employee(code, name, sal);
            curDepNode.add(new DefaultMutableTreeNode(newEmp));
        } else {
            Employee emp = (Employee) (curEmpNode.getUserObject());
            emp.setEmpName(name);
            emp.setSalary(sal);
        }
        this.cayQL.updateUI();
        this.addNewEmp = false;
    }//GEN-LAST:event_btnESaveActionPerformed

    private void btnSTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSTFActionPerformed
        if (root.getChildCount() == 0) {
            return;
        }
        String S;
        try {
            FileWriter f = new FileWriter(filename);
            PrintWriter pf = new PrintWriter(f);
            Enumeration depts = root.children();
            while (depts.hasMoreElements()) {
                DefaultMutableTreeNode depNode = (DefaultMutableTreeNode) depts.nextElement();
                Department dept = (Department) (depNode.getUserObject());
                S = dept.getDepCode() + "-" + dept.getDepName() + ":";
                pf.println(S);
                Enumeration emps = depNode.children();
                while (emps.hasMoreElements()) {
                    DefaultMutableTreeNode empNode = (DefaultMutableTreeNode) emps.nextElement();
                    Employee emp = (Employee) (empNode.getUserObject());
                    S = emp.getEmpCode() + "," + emp.getEmpName() + "," + emp.getSalary();
                    pf.println(S);
                }
            }
            pf.close();
            f.close();
            JOptionPane.showMessageDialog(this, "File đã được lưu tới file " + filename);
        } catch (HeadlessException | IOException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSTFActionPerformed

